#import "UIColor+FSUColorPallete.h"

@implementation UIColor (FSUColorPallete)

+ (UIColor *)garnetColor {
    return [UIColor colorWithRed:.329 green:.004 blue:.082 alpha:1.0];
}

+ (UIColor *)garnetButtonColor {
    return [UIColor colorWithRed:.482 green:.004 blue:.082 alpha:1.0];
}

+ (UIColor *)goldColor {
    return [UIColor colorWithRed:.804 green:.753 blue:.573 alpha:1.0];
}

+ (UIColor *)FSUGrayColor {
    return [UIColor colorWithRed:.31 green:.31 blue:.31 alpha:1.0];
}


@end
